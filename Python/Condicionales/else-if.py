condicion1 = True
condicion2 = True
condicion3 = True

if condicion1:
    print("se ejecuta si condicion1 es verdadera")
    
#En Python el else if se escribe elif
elif condicion2:
    print("se ejecuta si condicion1 es falsa y condicion2 es verdadera")

#Podemos hacer tantos como queramos
elif condicion3:
    print("se ejecuta si condicion1 y condicion2 es falsa y condicion3 es verdadera")

else:
    print("se ejecuta si condicion1, condicion2 y condicion3 son falsas")    
