#Para desempaquetar tenemos que tener en cuenta que:
# cant.de datos de la tupla == cant.de variables asignadas

#TUPLAS------------------------------------------------
#Creando una tupla
datos = ("Azul", "Petrocelli", 22)

#Desempaquetado en tupla
nombre,apellido,edad = datos

#Resultados de la tupla
print(nombre)
print(apellido)
print(edad)

#LISTAS------------------------------------------------
#Creando una lista
datos = ["Azul", "Petrocelli", 22]

#Desempaquetado en lista
nombre,apellido,edad = datos

#Resultados de la lista
print(nombre)
print(apellido)
print(edad)
